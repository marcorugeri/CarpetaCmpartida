public class DoWhile {

    public static void main(String[] args) {
        
    /*
        Sintaxis

        do {

            codigo que se repetira mientras se cumpla la condicion dad

        while(condicion);
        }
    */

        int x = 20;

        do {

            System.out.println(x);
            x++;
        
        }while(x <= 10);



    }
    
}
